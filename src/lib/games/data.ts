import type { BangumiUserCollection } from '@/types/bangumi';

function game(
  id: number,
  name: string,
  name_cn: string,
  date: string,
  score: number,
  largeImg: string,
  tags: string[],
): BangumiUserCollection {
  return {
    subject_id: id,
    subject_type: 4,
    rate: 0,
    type: 2,
    tags,
    ep_status: 0,
    vol_status: 0,
    updated_at: '',
    subject: {
      id,
      type: 4,
      name,
      name_cn,
      date,
      images: {
        large: largeImg,
        common: largeImg.replace('/pic/cover/l/', '/r/400/pic/cover/l/'),
        medium: largeImg.replace('/pic/cover/l/', '/r/800/pic/cover/l/'),
        small: largeImg.replace('/pic/cover/l/', '/r/200/pic/cover/l/'),
        grid: largeImg.replace('/pic/cover/l/', '/r/100/pic/cover/l/'),
      },
      score,
      tags: tags.map((t) => ({ name: t, count: 0 })),
    },
  };
}

